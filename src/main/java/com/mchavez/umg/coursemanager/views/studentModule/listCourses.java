    public void actualizarTabla() {
        DefaultTableModel data = new DefaultTableModel(new String[]{"Id", "Nombre", "Sección", "Fecha Inicio", "Fecha Fin", "Profesor"}, 0);
        jTable1.setModel(data);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");

        for (Course course : Coursemanager.courses) {
            boolean isEnrolled = false;
            for (Student student : course.getStudents()) {
                if (student.getUser().equals(studentSelected.getUser())) {
                    isEnrolled = true;
                    break;
                }
            }
            if (isEnrolled) {
                Object[] rowData = {
                    course.getId(),
                    course.getName(),
                    course.getSection(),
                    sdf.format(course.getStartDate()),
                    sdf.format(course.getEndDate()),
                    course.getTeacher()
                };
                data.addRow(rowData);
            }
        }

    }
    
    private void eliminarEstudianteDeCurso(int courseId, String studentName) {
        for (Course curso : Coursemanager.courses) {
            if (curso.getId() == courseId) {
                for (Iterator<Student> iterator = curso.students.iterator(); iterator.hasNext();) {
                    Student student = iterator.next();
                    if (student.getName().equals(studentName)) {
                        iterator.remove();
                        break;
                    }
                }
                break;
            }
        }
    }


