    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Course courseSelected = null;
        Student studentSelected = Coursemanager.studentLoged;

        for (Course c : Coursemanager.courses) {
            if (c.getName().equals(jComboBox1.getSelectedItem().toString())) {
                courseSelected = c;
                break;
            }
        }

        if (studentSelected != null && courseSelected != null) {
            boolean alreadyAssigned = false;
            for (Student s : courseSelected.getStudents()) {
                if (s.getUser().equals(studentSelected.getUser())) {
                    alreadyAssigned = true;
                    break;
                }
            }

            if (alreadyAssigned) {
                JOptionPane.showMessageDialog(this, "El estudiante ya está asignado a este curso.",
                        "Operación fallida", JOptionPane.ERROR_MESSAGE);
            } else if (courseSelected.getStudents().size() >= 10) {
                JOptionPane.showMessageDialog(this, "No se pueden asignar más de 10 estudiantes a un curso.",
                        "Operación fallida", JOptionPane.ERROR_MESSAGE);
            } else if (getCourseCountForStudent(studentSelected) >= 5) {
                JOptionPane.showMessageDialog(this, "Un estudiante no puede estar asignado a más de 5 cursos.",
                        "Operación fallida", JOptionPane.ERROR_MESSAGE);
            } else {
                courseSelected.getStudents().add(studentSelected);
                JOptionPane.showMessageDialog(this, "Curso asignado exitosamente.",
                        "Éxito", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo encontrar el curso o el estudiante.",
                    "Operación fallida", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

      private int getCourseCountForStudent(Student student) {
        int count = 0;
        for (Course c : Coursemanager.courses) {
            if (c.getStudents().contains(student)) {
                count++;
            }
        }
        return count;
    }


